package Roguelike;

public abstract class Creature extends Object{
	public abstract void setLeve(int leve);
	public abstract int getLeve();
	public abstract void setAttack(int attack);
	public abstract int getAttack();
	public abstract void setMP(int MP);
	public abstract int getMP();
	public abstract void setHP(int HP);
	public abstract int getHP();
	public abstract void setPosX(int poX);
	public abstract int getPosX();
	public abstract void setPosY(int poY);
	public abstract int getPosY();
	
}
